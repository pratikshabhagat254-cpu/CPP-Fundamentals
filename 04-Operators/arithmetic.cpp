#include <iostream>
using namespace std;

int main() {

    int a = 10;   // First integer value
    int b = 3;    // Second integer value

    cout << "Addition: " << a + b << endl;
    // Adds both numbers

    cout << "Subtraction: " << a - b << endl;
    // Subtracts b from a

    cout << "Multiplication: " << a * b << endl;
    // Multiplies both numbers

    cout << "Division: " << a / b << endl;
    // Divides a by b and gives quotient

    cout << "Modulus: " << a % b << endl;
    // Gives remainder after division

    return 0;
}