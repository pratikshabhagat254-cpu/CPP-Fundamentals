#include <iostream>
using namespace std;

int main() {

    int a = 10;   // First integer value
    int b = 20;   // Second integer value

    cout << "a == b : " << (a == b) << endl;
    // Checks if both values are equal

    cout << "a != b : " << (a != b) << endl;
    // Checks if both values are not equal

    cout << "a > b  : " << (a > b) << endl;
    // Checks if a is greater than b

    cout << "a < b  : " << (a < b) << endl;
    // Checks if a is smaller than b

    cout << "a >= b : " << (a >= b) << endl;
    // Checks if a is greater than or equal to b

    cout << "a <= b : " << (a <= b) << endl;
    // Checks if a is smaller than or equal to b

    return 0;
}