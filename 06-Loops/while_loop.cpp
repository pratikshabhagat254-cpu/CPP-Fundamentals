#include <iostream>
using namespace std;

int main() 
{

    int i = 1;   // Starting value of loop variable

    while(i <= 5) {
        // Loop runs while i is less than or equal to 5

        cout << i << " ";
        // Displays value of i on the same line

        i++;
        // Increases value of i by 1
    }

    return 0;
}