#include <iostream>
using namespace std;

int main() {

    cout << "Size of char: " << sizeof(char) << " bytes" << endl;
    // sizeof(char) gives the memory size of char data type

    cout << "Size of int: " << sizeof(int) << " bytes" << endl;
    // sizeof(int) gives the memory size of int data type

    cout << "Size of float: " << sizeof(float) << " bytes" << endl;
    // sizeof(float) gives the memory size of float data type

    cout << "Size of double: " << sizeof(double) << " bytes" << endl;
    // sizeof(double) gives the memory size of double data type

    cout << "Size of bool: " << sizeof(bool) << " bytes" << endl;
    // sizeof(bool) gives the memory size of bool data type

    return 0;
}