#include <iostream>
using namespace std;

int main() {

    signed int temperature = -15;
    // signed int can store both positive and negative numbers

    unsigned int population = 50000;
    // unsigned int stores only positive numbers

    cout << "Signed Integer: " << temperature << endl;
    // Displays the signed integer value

    cout << "Unsigned Integer: " << population << endl;
    // Displays the unsigned integer value

    return 0;
}